package kr.or.ddit.dtkkiri.vo;

public class ReplyVO {
	private int rpy_num;
	private int rpy_pnum;
	private String rpy_date;
	private String rpy_cont;
	private String rpy_id;
	
	public int getRpy_num() {
		return rpy_num;
	}
	public void setRpy_num(int rpy_num) {
		this.rpy_num = rpy_num;
	}
	public int getRpy_pnum() {
		return rpy_pnum;
	}
	public void setRpy_pnum(int rpy_pnum) {
		this.rpy_pnum = rpy_pnum;
	}
	public String getRpy_date() {
		return rpy_date;
	}
	public void setRpy_date(String rpy_date) {
		this.rpy_date = rpy_date;
	}
	public String getRpy_cont() {
		return rpy_cont;
	}
	public void setRpy_cont(String rpy_cont) {
		this.rpy_cont = rpy_cont;
	}
	public String getRpy_id() {
		return rpy_id;
	}
	public void setRpy_id(String rpy_id) {
		this.rpy_id = rpy_id;
	}
}
