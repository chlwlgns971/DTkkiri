package kr.or.ddit.dtkkiri.vo;

public class BlacklistVO {

	private String bkl_date;
	private String bkl_rsn;
	private String bkl_mgr;
	private String bkl_mail;
	
	// getter / setter
	public String getBkl_date() {
		return bkl_date;
	}
	public void setBkl_date(String bkl_date) {
		this.bkl_date = bkl_date;
	}
	public String getBkl_rsn() {
		return bkl_rsn;
	}
	public void setBkl_rsn(String bkl_rsn) {
		this.bkl_rsn = bkl_rsn;
	}
	public String getBkl_mgr() {
		return bkl_mgr;
	}
	public void setBkl_mgr(String bkl_mgr) {
		this.bkl_mgr = bkl_mgr;
	}
	public String getBkl_mail() {
		return bkl_mail;
	}
	public void setBkl_mail(String bkl_mail) {
		this.bkl_mail = bkl_mail;
	}
	
}
