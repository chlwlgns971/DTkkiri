package kr.or.ddit.dtkkiri.vo;

public class PostVO {
	//게시글 테이블VO
	private int post_num;
	private String post_tit;
	private String post_date;
	private String post_cont;
	private int post_view_num;
	private int post_rec_num;
	private String post_bcode;
	private String post_id;
	
	public int getPost_num() {
		return post_num;
	}
	public void setPost_num(int post_num) {
		this.post_num = post_num;
	}
	public String getPost_tit() {
		return post_tit;
	}
	public void setPost_tit(String post_tit) {
		this.post_tit = post_tit;
	}
	public String getPost_date() {
		return post_date;
	}
	public void setPost_date(String post_date) {
		this.post_date = post_date;
	}
	public String getPost_cont() {
		return post_cont;
	}
	public void setPost_cont(String post_cont) {
		this.post_cont = post_cont;
	}
	public int getPost_view_num() {
		return post_view_num;
	}
	public void setPost_view_num(int post_view_num) {
		this.post_view_num = post_view_num;
	}
	public int getPost_rec_num() {
		return post_rec_num;
	}
	public void setPost_rec_num(int post_rec_num) {
		this.post_rec_num = post_rec_num;
	}
	public String getPost_bcode() {
		return post_bcode;
	}
	public void setPost_bcode(String post_bcode) {
		this.post_bcode = post_bcode;
	}
	public String getPost_id() {
		return post_id;
	}
	public void setPost_id(String post_id) {
		this.post_id = post_id;
	}
	
	
	
}
