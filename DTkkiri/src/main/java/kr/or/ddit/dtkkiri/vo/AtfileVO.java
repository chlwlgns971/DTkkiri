package kr.or.ddit.dtkkiri.vo;

public class AtfileVO {
	private String atfile_sv_name;
	private String atfile_og_name;
	private String atfile_loc;
	private long atfile_size;
	private int atfile_pnum;
	
	public String getAtfile_sv_name() {
		return atfile_sv_name;
	}
	public void setAtfile_sv_name(String atfile_sv_name) {
		this.atfile_sv_name = atfile_sv_name;
	}
	public String getAtfile_og_name() {
		return atfile_og_name;
	}
	public void setAtfile_og_name(String atfile_og_name) {
		this.atfile_og_name = atfile_og_name;
	}
	public String getAtfile_loc() {
		return atfile_loc;
	}
	public void setAtfile_loc(String atfile_loc) {
		this.atfile_loc = atfile_loc;
	}
	public long getAtfile_size() {
		return atfile_size;
	}
	public void setAtfile_size(long atfile_size) {
		this.atfile_size = atfile_size;
	}
	public int getAtfile_pnum() {
		return atfile_pnum;
	}
	public void setAtfile_pnum(int atfile_pnum) {
		this.atfile_pnum = atfile_pnum;
	}
	
}
