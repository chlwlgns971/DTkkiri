package kr.or.ddit.dtkkiri.vo;

public class ReminderVO {
	// 리마인더 테이블 VO
	private String rmd_cont;
	private String rmd_date;
	private int rmd_num;
	private String rmd_id;
	
	public String getRmd_cont() {
		return rmd_cont;
	}
	public void setRmd_cont(String rmd_cont) {
		this.rmd_cont = rmd_cont;
	}
	public String getRmd_date() {
		return rmd_date;
	}
	public void setRmd_date(String rmd_date) {
		this.rmd_date = rmd_date;
	}
	public int getRmd_num() {
		return rmd_num;
	}
	public void setRmd_num(int rmd_num) {
		this.rmd_num = rmd_num;
	}
	public String getRmd_id() {
		return rmd_id;
	}
	public void setRmd_id(String rmd_id) {
		this.rmd_id = rmd_id;
	}
	
	
}
